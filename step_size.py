# For loop step size 
for i in range(0,40,2):
    print(i)
    i+=2
else:
    print("Done")

# For loop in list 
list =[3,4,5,6,7,8,9]
for i in list:
    print(i)
    i=i+1
else:
    print("Done")

# for loop in Tupple 
tupple=(2,34,56,78,78)
for i in tupple:
    print(i)
    i=i+1
else:
    print("Done")

# for loop in set 
set={3,3,4,5,6,6,7,8}
for i in set:
    print(i)
    i=i+1
else:
    print("Done")

# for loop in string 
s="Shashikant"
for i in s:
    print(i)
else:
    print("Done")
 



